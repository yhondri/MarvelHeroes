public enum HttpMethod: String {
    case get     = "GET"
    case head    = "HEAD"
    case post    = "POST"
    case put     = "PUT"
    case patch   = "PATCH"
    case delete  = "DELETE"
    case connect = "CONNECT"
    case options = "OPTIONS"
    case trace   = "TRACE"
}
